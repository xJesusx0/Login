    public static String ObtenerFecha(){
        LocalDate fechaActual = LocalDate.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        return fechaActual.format(formato);
    }
    
    public static void GuardarHistorial(String ruta , String Resultado , double Base , double Altura) throws IOException{
        PrintWriter file = new PrintWriter(new FileWriter(ruta,true));
        file.println("===============");
        file.println( "Fecha : " + ObtenerFecha());
        file.println("---- Datos ----");
        file.println("Base : " + Base);
        file.println("Altura : " + Altura);
        file.println("---- Resultado ----");
        file.println(Resultado);
        file.close();
        
    }
    
    public static double CalcularPerimetro(double Base, double Altura){
        return (Base * 2 + Altura *2);
    }
    
    public static double CalcularArea(double Base, double Altura){
        return(Base * Altura);
    }
    
