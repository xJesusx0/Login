    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed

        if(ValidarFormulario(TxtBase.getText()).equals("Error") || TxtAltura.getText().equals("Error")){
            JOptionPane.showMessageDialog(null, "Entradas vacias verifique el formulario");
        } else {
            double Base = Double.parseDouble(TxtBase.getText());
            double Altura = Double.parseDouble(TxtAltura.getText());

            String Resultado = "El Area es " + String.valueOf(CalcularArea(Base, Altura)) + " Perimetro " + String.valueOf(CalcularPerimetro(Base, Altura));
            TxtResultado.setText(Resultado);
            try {
                GuardarHistorial("Historial.txt", Resultado, Base, Altura);
            } catch (IOException ex) {
                Logger.getLogger(NewJFrameRectangulo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_BtnCalcularActionPerformed

